package fr.supinfo.java.spawn;

public class ObjPosition {

    int x;
    int y;

    public ObjPosition(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public void setX(int newX) {
        x = newX;
    }

    public void setY(int newY) {
        y = newY;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
